/**
 * fetch-frontier — pull the Artificial Analysis "Frontier Language Model
 * Intelligence over Time" dataset and bake it into the project.
 * ──────────────────────────────────────────────────────────────────────────
 * Queries the AA API (https://artificialanalysis.ai/api/v2/data/llms/models),
 * keeps the labs AA plots on its frontier chart, collapses each model family to
 * its **max-effort** variant (the headline Intelligence Index AA highlights),
 * and writes a committed TS module the chart imports — so prints render offline.
 *
 *   node scripts/fetch-frontier.mjs        # refresh src/print/space/frontier-data.generated.ts
 *   npm run frontier:fetch
 *
 * Needs ARTIFICIAL_ANALYSIS_API_KEY in .env (see .env.example). Source to cite:
 * Artificial Analysis — https://artificialanalysis.ai (Intelligence Index v4.0).
 */
import { writeFileSync } from 'node:fs'
import path from 'node:path'

const ROOT = process.cwd()
const ENDPOINT = 'https://artificialanalysis.ai/api/v2/data/llms/models'
const OUT = path.join(ROOT, 'src/print/space/frontier-data.generated.ts')
const INDEX_VERSION = '4.0'

/**
 * The labs AA shows on the frontier chart, in legend order, with AA's exact
 * marker colours (sampled from artificialanalysis.ai). A model whose creator is
 * not here is dropped — this is the curated frontier set, not all 49 creators.
 */
const PROVIDERS = [
  ['OpenAI', { key: 'openai', label: 'OpenAI', color: '#1f1f1f' }],
  ['xAI', { key: 'xai', label: 'xAI', color: '#736cd3' }],
  ['Meta', { key: 'meta', label: 'Meta', color: '#0089f4' }],
  ['Google', { key: 'google', label: 'Google', color: '#34a853' }],
  ['Anthropic', { key: 'anthropic', label: 'Anthropic', color: '#cc785c' }],
  ['Mistral', { key: 'mistral', label: 'Mistral', color: '#fd6f00' }],
  ['DeepSeek', { key: 'deepseek', label: 'DeepSeek', color: '#2243e6' }],
  ['Upstage', { key: 'upstage', label: 'Upstage', color: '#7c59f5' }],
  ['MiniMax', { key: 'minimax', label: 'MiniMax', color: '#eb3568' }],
  ['Kimi', { key: 'kimi', label: 'Kimi', color: '#047afe' }],
  ['Xiaomi', { key: 'xiaomi', label: 'Xiaomi', color: '#ff6900' }],
  ['Alibaba', { key: 'alibaba', label: 'Alibaba', color: '#ff7018' }],
  ['MBZUAI Institute of Foundation Models', { key: 'mbzuai', label: 'MBZUAI', color: '#1521a9' }],
  ['Z AI', { key: 'zai', label: 'Z AI', color: '#1c7ff8' }],
]
const PROVIDER_BY_CREATOR = new Map(PROVIDERS)

const familyOf = (name) => name.replace(/\s*\([^)]*\)\s*$/, '').trim()
const isReasoning = (name) => /reason|thinking/i.test(name)
const slug = (s) =>
  s
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')

async function main() {
  try {
    process.loadEnvFile(path.join(ROOT, '.env'))
  } catch {
    /* no .env — rely on the ambient environment */
  }
  const key = process.env.ARTIFICIAL_ANALYSIS_API_KEY
  if (!key) {
    console.error('Missing ARTIFICIAL_ANALYSIS_API_KEY. Add it to .env (see .env.example).')
    process.exit(1)
  }

  const res = await fetch(ENDPOINT, { headers: { 'x-api-key': key } })
  if (!res.ok) {
    console.error(`AA API ${res.status} ${res.statusText}`)
    process.exit(1)
  }
  const json = await res.json()
  const models = json.data ?? []

  // Collapse to one point per (creator, family): the max-Intelligence-Index
  // variant; a family is "reasoning" if any of its variants is.
  const fams = new Map()
  for (const m of models) {
    const idx = m.evaluations?.artificial_analysis_intelligence_index
    if (idx == null) continue
    const creator = m.model_creator?.name
    const prov = PROVIDER_BY_CREATOR.get(creator)
    if (!prov) continue
    const family = familyOf(m.name)
    const id = `${prov.key}-${slug(family)}`
    const reasoning = isReasoning(m.name)
    const cur = fams.get(id)
    if (!cur || idx > cur.index) {
      fams.set(id, { id, label: family, provider: prov.key, date: m.release_date, index: idx, reasoning: reasoning || cur?.reasoning || false })
    } else if (reasoning) {
      cur.reasoning = true
    }
  }

  const points = [...fams.values()]
    .filter((p) => p.date)
    .sort((a, b) => (a.date < b.date ? -1 : a.date > b.date ? 1 : a.index - b.index))

  const providersOut = PROVIDERS.map(([, p]) => p)
  const today = new Date().toISOString().slice(0, 10)

  const lines = []
  lines.push('/**')
  lines.push(' * frontier-data.generated — AUTO-GENERATED by scripts/fetch-frontier.mjs.')
  lines.push(' * Do NOT edit by hand; run `npm run frontier:fetch` to refresh.')
  lines.push(' *')
  lines.push(' * Source: Artificial Analysis (https://artificialanalysis.ai),')
  lines.push(` * Intelligence Index v${INDEX_VERSION}. One point per model family = its`)
  lines.push(' * max-effort variant (the headline index AA plots).')
  lines.push(' */')
  lines.push('')
  lines.push("export type FrontierProvider =")
  lines.push('  ' + providersOut.map((p) => `'${p.key}'`).join(' | '))
  lines.push('')
  lines.push('export type FrontierRaw = {')
  lines.push('  id: string')
  lines.push('  label: string')
  lines.push('  provider: FrontierProvider')
  lines.push('  /** Release date, YYYY-MM-DD. */')
  lines.push('  date: string')
  lines.push('  /** Artificial Analysis Intelligence Index. */')
  lines.push('  index: number')
  lines.push('  reasoning: boolean')
  lines.push('}')
  lines.push('')
  lines.push(`export const FRONTIER_GENERATED_AT = '${today}'`)
  lines.push(`export const FRONTIER_INDEX_VERSION = '${INDEX_VERSION}'`)
  lines.push('')
  lines.push('/** Labs AA plots, in legend order, with AA\'s exact marker colours. */')
  lines.push('export const FRONTIER_PROVIDERS: Record<FrontierProvider, { label: string; color: string }> = {')
  for (const p of providersOut) lines.push(`  ${p.key}: { label: ${JSON.stringify(p.label)}, color: '${p.color}' },`)
  lines.push('}')
  lines.push('')
  lines.push(`/** ${points.length} model families · ${providersOut.length} labs · `)
  lines.push(` *  ${points[0]?.date} → ${points[points.length - 1]?.date}. */`)
  lines.push('export const FRONTIER_RAW: FrontierRaw[] = [')
  for (const p of points) {
    const r = p.reasoning ? ', reasoning: true' : ', reasoning: false'
    lines.push(`  { id: '${p.id}', label: ${JSON.stringify(p.label)}, provider: '${p.provider}', date: '${p.date}', index: ${p.index}${r} },`)
  }
  lines.push(']')
  lines.push('')

  writeFileSync(OUT, lines.join('\n'))
  console.log(`Wrote ${points.length} families (${providersOut.length} labs) → ${path.relative(ROOT, OUT)}`)
  console.log(`Range ${points[0]?.date} → ${points[points.length - 1]?.date} · ${points.filter((p) => p.reasoning).length} reasoning`)
}

main().catch((e) => {
  console.error(e)
  process.exit(1)
})
